async function getTokenIds(contract: ethers.Contract, account: string) {
  const balance = await contract.balanceOf(account);
  const result = [];
  for (let i = 0; i < balance.toNumber(); i++) {
    result.push((await contract.tokenOfOwnerByIndex(account, i)).toString());
  }
  return result;
}

async function getTokensMetadata(contract: ethers.Contract, tokenIds: Array<string>) {
  const result = [];
  for (let tokenId of tokenIds) {
    result.push(await contract.tokenURI(tokenId));
  }
  return result;
}

async function doStuff(indexerApi: IndexerApi, ethState: Connected) {
  const configuration = await indexerApi.getConfiguration();
  const erc721Tokens = configuration.tokens.filter(t => t.type === 'ERC721');
  const firstERC721Tokens = erc721Tokens[0];
  const contract = new ethers.Contract(firstERC721Tokens.ethereumContractAddress, abi, ethState.ethereumToolkit);
  const tokenIds = await getTokenIds(contract, ethState.ethereumAccount);
  const metadatas = await getTokensMetadata(contract, tokenIds);
  console.log(metadatas);
}

